# Calcular la circunferencia de un círculo dado su radio

import math

radio = 3
circunferencia = 2 * math.pi * radio
# Redondea el valor de la circunferencia a 6 decimales
circunferencia_redondeada = round(circunferencia, 6)

print("La circunferencia de un círculo con radio", radio, "es:", circunferencia_redondeada)

